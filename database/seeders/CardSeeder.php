    /**
     * Scale positive effects proportionally to difficulty.
     * Low difficulty = small rewards, high difficulty = big rewards.
     */
    private function balancePositiveEffects(array $card): array
    {
        $diff = $card['difficulty'];
        $effects = $card['positive_effects'];

        // Preserve special keys (non-stat keys)
        $specialKeys = [];
        foreach (['grant_item_id', 'draw_item', 'recover_die'] as $key) {
            if (isset($effects[$key])) {
                $specialKeys[$key] = $effects[$key];
                unset($effects[$key]);
            }
        }

        // Target total positive value based on difficulty
        $targetTotal = match ($diff) {
            3 => 2,
            4 => 2,
            5 => 3,
            6 => 3,
            7 => 4,
            8 => 5,
            9 => 5,
            10 => 6,
            11 => 6,
            12 => 7,
            default => max(2, (int) round($diff * 0.55)),
        };

        $numStats = count($effects);

        if ($numStats === 1) {
            $stat = array_key_first($effects);
            $effects[$stat] = $targetTotal;
        } elseif ($numStats >= 2) {
            $keys = array_keys($effects);
            $remaining = $targetTotal;

            for ($i = 0; $i < $numStats; $i++) {
                if ($i === $numStats - 1) {
                    $effects[$keys[$i]] = max(1, $remaining);
                } else {
                    $share = (int) ceil($remaining / ($numStats - $i));
                    $effects[$keys[$i]] = max(1, $share);
                    $remaining -= $effects[$keys[$i]];
                }
            }
        }

        foreach ($specialKeys as $key => $value) {
            $effects[$key] = $value;
        }

        $card['positive_effects'] = $effects;

        return $card;
    }
